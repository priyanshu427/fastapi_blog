# StarletteHTTPException Handler
@app.exception_handler(StarletteHTTPException)
async def general_http_exception_handler(request: Request, exception: StarletteHTTPException):
   
    if request.url.path.startswith("/api"):
        return  await http_exception_handler(request,exception)  # http_exception_handler is a async function and transmitting the http response across a internet socket takes i/o operation we use await so it can process incoming requests for users while the cpu hands over the packets to uvicorn and then to the linux kernel to broadcast.
                                                                 # request for all metadata like url ,ip,socket pipeline of that user the function inspects it . exception has http status like 404(exception.status code) and a message along it like username already exists(exception.detail)
    message = (              # message only needed for templates
        exception.detail     #  exception.detail it provides the actual text message that goes inside both of those blocks for api and user routes. variable message holds that exception which is then used by our routes.
        if exception.detail                                                 # without ternary operator  # if exception.detail:  # message = exception.detail
        else "An error occurred. Please check your request and try again."  # else:  # message = "An error occurred. Please check your request."
    )                                                                       # here exception.detail is only returned if its true like a text message of post not found is there otherwise else
         
    return templates.TemplateResponse(  # this function is sync as all it does it compiling text in ram like jinga replacing the values and packages it to uvicorn which is async that uses an internal await . this code itself isnt streaming anything
        request,
        "error.html",
        {
            "status_code": exception.status_code,
            "title": exception.status_code,
            "message": message,
        },
        status_code=exception.status_code,
    )                                          # api routes should return json and other routes html page.

