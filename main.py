@app.exception_handler(StarletteHTTPException)
def general_http_exception_handler(request: Request, exception: StarletteHTTPException):
    message = (
        exception.detail
        if exception.detail
        else "An error occurred. Please check your request and try again."
        # without ternary operator
        # if exception.detail:
        #     message = exception.detail
        # else:
        #     message = "An error occurred. Please check your request."
        )
# exception.detail it provides the actual text message that goes inside both of those blocks for api and user routes. variable message holds that exception which is then used by our routes.
    if request.url.path.startswith("/api"):
        return JSONResponse(
            status_code=exception.status_code,
            content={"detail": message},
        )
    return templates.TemplateResponse(
        request,
        "error.html",
        {
            "status_code": exception.status_code,
            "title": exception.status_code,
            "message": message,
        },
        status_code=exception.status_code,
    )
