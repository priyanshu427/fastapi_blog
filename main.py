@app.post(
    "/api/users",
    response_model=UserResponse,
    status_code=status.HTTP_201_CREATED,
) 
def create_user(user: UserCreate,db: Annotated[Session,Depends (get_db)]): # (dependency injection) before running the function get db is called and gives a db session and close when the request is completed.
    result= db.execute(select(models.User).where(models.User.username==user.username), # a select query to check if the user exists. we compare the user.username that we are trying to create to the usernames in db
    )
    existing_user=result.scalars().first() # get the first user object(here object has everthing like id,ussername,etc) if there is one (existing_user becomes a instance of the user class if there is a match) or none if the user doesnt exist

    if existing_user:
         raise HTTPException(
              status_code=status.HTTP_400_BAD_REQUEST,
              detail="Username already exists"
         )
    
    result= db.execute(select(models.User).where(models.User.email==user.email), # checking for existing email
    )
    existing_email=result.scalars().first()  # we dont use existing_user.email() to check as a none earlier will crash everything

    if existing_email:
         raise HTTPException(
              status_code=status.HTTP_400_BAD_REQUEST,
              detail="Email already exists"
         )
    
    new_user=models.User( # creating new user
         username=user.username,
         email=user.email,
         )
    db.add(new_user) 
    db.commit()
    db.refresh(new_user)
# add inserts the user while commit executes it and saves it. refresh reloads the object from db.
    return new_user

